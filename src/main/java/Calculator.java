
public class Calculator {
    public static boolean add(int a, int b, int result){
        return a+b==result;
    }
    public static boolean sub(int a, int b, int result){
        return a-b==result;
    }
    public static boolean pro(int a, int b, int result){
        return a*b==result;
    }
    public static boolean divide(int a, int b, int result){
        return a/b==result;
    }
}